package com.dcaex.spbc.dto;

public class Transactions {
	
	private String txHash;
	private String txReceiptStatus;
	private String block;
	private String age;
	private String from;
	private String to;
	private String value;
	private String txFree;
	private String gasLimit;
	private String gasUsed;
	private String gasPrice;
	private String actualTxCost;
	private String nonce;
	private String inputData;
	
	public String getTxHash() {
		return txHash;
	}
	public void setTxHash(String txHash) {
		this.txHash = txHash;
	}
	public String getTxReceiptStatus() {
		return txReceiptStatus;
	}
	public void setTxReceiptStatus(String txReceiptStatus) {
		this.txReceiptStatus = txReceiptStatus;
	}
	public String getBlock() {
		return block;
	}
	public void setBlock(String block) {
		this.block = block;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getTxFree() {
		return txFree;
	}
	public void setTxFree(String txFree) {
		this.txFree = txFree;
	}
	public String getGasLimit() {
		return gasLimit;
	}
	public void setGasLimit(String gasLimit) {
		this.gasLimit = gasLimit;
	}
	public String getGasUsed() {
		return gasUsed;
	}
	public void setGasUsed(String gasUsed) {
		this.gasUsed = gasUsed;
	}
	public String getGasPrice() {
		return gasPrice;
	}
	public void setGasPrice(String gasPrice) {
		this.gasPrice = gasPrice;
	}
	public String getActualTxCost() {
		return actualTxCost;
	}
	public void setActualTxCost(String actualTxCost) {
		this.actualTxCost = actualTxCost;
	}
	public String getNonce() {
		return nonce;
	}
	public void setNonce(String nonce) {
		this.nonce = nonce;
	}
	public String getInputData() {
		return inputData;
	}
	public void setInputData(String inputData) {
		this.inputData = inputData;
	}
	
	
	
	
}
